def build_chat_prompt(history: list[dict], user_message: str) -> str:
    """把最近的对话历史注入到本轮 prompt（容器无状态，不依赖 SDK 会话文件）。"""
    if not history:
        return user_message

    lines = ["以下是你与该用户此前的对话记录，仅供理解上下文，不要重复回答："]
    for message in history:
        speaker = "用户" if message["role"] == "user" else "你"
        lines.append(f"{speaker}：{message['content']}")
    lines.append("")
    lines.append(f"用户的新消息：{user_message}")
    return "\n".join(lines)
